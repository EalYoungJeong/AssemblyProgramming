	LDR r1, Matrix_padding	;address of Matrix_padding
	

Padding
	LDR r2, [r0]			;padding first of firstline
	STR r2, [r1], #4	;store first in matrix padding
	MOV r3, #0			;count = 0
	
Padding_firstline
	LDR r2, [r0], #4	;load from matrix first
	STR r2, [r1], #4	;store r2 in matrix padding
	ADD r3, r3, #1		;count ++
	CMP r3, #64			;if count == 64
	BNE Padding_firstline	;loop again
	LDREQ r2, [r0, #-4]		;last of firstline
	STREQ r2, [r1], #4		;store last
	LDREQ r0, =Matrix_first	;get address of matrix_first
	MOVEQ r3, #0		;column index = 0
	MOVEQ r4, #0		;row index = 0
	BEQ Padding_middle	;go Padding_middle

Padding_middle		;make middle parto f Matrix_padding
	CMP r4, #64			;is it last line?
	;no
	LDRNE r2, [r0]		;get first of line
	STRNE r2, [r1], #4	;store first in Matrix_padding
	MOVNE r5, r0
	BNE Padding_middle_copy	;go Padding_middle_copy
	;yes
	LDREQ r2, [r5]		;get first of last line
	STREQ r2, [r1], #4	;store first in Matrix_padding
	BEQ Padding_lastline	;go Padding_lastline

Padding_middle_copy	;make one line in Matrix_padding
	LDR r2, [r0], #4	
	STR r2, [r1], #4	;copy Matrix first into Matrix_padding
	ADD r3, r3, #1	;column index++
	CMP r3, #64		;does it loop 64 times?
	;no
	BNE Padding_middle_copy	;copy again
	;yes
	LDREQ r2, [r0, #-4]	;get last one in line
	STREQ r2, [r1], #4	;store last in Matrix_padding
	MOVEQ r3, #0		;column index initialization
	ADDEQ r4, r4, #1	;row index++
	BEQ Padding_middle	;go Padding_middle
	
Padding_lastline	;make last line of Matrix_padding
	LDR r2, [r5], #4	
	STR r2, [r1], #4	;copy last line in Matrix_padding
	ADD r3, r3, #1
	CMP r3, #64	;does it loop 64 times?
	;no
	BNE Padding_lastline	;copy again
	;yes
	LDREQ r2, [r5, #-4]	;get last of last line
	STREQ r2, [r1], #4	;store last in Matrix_padding
	
Convolution
	LDR r0, Matrix_padding	;address of Matrix_padding
	LDR r1, =Matrix_second	;address of Matrix_second
	LDR r2, Matrix_convolution	;address to save Matrix_convolution
	LDR r8, convolution_temp		;temporary address for cumulative addition
	MOV r3, #0	;column index = 0
	MOV r4, #0	;row index = 0
	
Convolution_start
	CMP r3, #64	;does it loop 64 times?
	;no
	BNE Convolution_one	;go convolution operation
	;yes
	ADDEQ r4, r4, #1	;row index++
	CMPEQ r4, #64		;does row loop 64 times?
	;no
	MOVNE r3, #0	;column index initialization
	ADDNE r0, r0, #8	;r0 is address that first of next line
	BNE Convolution_one	;go convolution operatoin
	;yes
	BEQ sub_sampling	;go sub_sampling

Convolution_one	;convolution operation start
	LDR r9, [r0]			;r0[0,0]
	LDR r10, [r1], #4	;second[0,0]
	BL multiplication	;r9*r10 = r7
	STR r7, [r8]			;store r7 in temp address
	
Convolution_two	
	LDR r9, [r0, #4]	;r0[0,1]
	LDR r10, [r1], #4	;second[0,1]
	BL multiplication				;r9*r10 = r7
	LDR r9, [r8]			;get convolution one result to cumulative addition
	BL adding			;r7 = r7+r9	;addition
	STR r7, [r8]		;store r7 in temp address
	
Convolution_three
