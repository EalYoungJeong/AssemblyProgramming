	ADD r0, r0, #4;padding address++
	LDR r1, =Matrix_second
	B Convolution_start	;go Convolution_start
	
multiplication
	STMFD sp!, {r0-r4, r8, lr}
	
	MOV r6, #0	;for fraction multiplication
	MOV r7, #0	;initialize result register
	
	LDR r11, =0x80000000 ; get sign bit
	AND r0, r9, r11	;op1 sign bit
	AND r1, r10, r11 ;op2 sign bit
	EOR r0, r0, r1	;result sign bit
	
	LDR r11, =0x7F800000;get exponent bit
	AND r2, r9, r11			;op1 exp bit
	AND r3, r10, r11		;op2 exp bit
	ADD r2, r2, r3			;r2=r3+r2
	MOV r1, #127;			;to -127
	LSL r1, #23				;make 127+0*(23bit)
	SUB r2, r2, r1			;r3+r2-127, result of exp bit
	
	
	LDR r11, =0x007FFFFF;get fraction bit
	AND r4, r9, r11			;op1 fraction bit
	AND r5, r10, r11		;op2 fraction bit
	LDR r11, =0x00800000; make 1.0
	ADD r4, r4, r11			;make 1.fraction, for X
	ADD r5, r5, r11			;make 1.fraction, for A
	
	LSR r4, #16	;X for  mul
	LDR r11, =0x00000001	;get add sign bit
	MOV r1, #0		;multiplicand count
	MOV r3, #0		;to get 1bit from r1
	MOV r8, r5		;to shift multiplier
	BL fraction_mul
	
	LDR r11, =0x007FFFFF	;except	1.0 
	AND r6, r6, r11				;delete 1.0 from fraction mul result
	ADD r7, r0, r2	;combine sign and exponent
	LSL r12, #23		;make shift stack 
	ADD r7, r7, r12	;add shift stack to exponent bit
	ADD r7, r7, r6	;combine fraction
	

	LDMFD sp!, {r0-r4, r8, pc}
	
fraction_mul
	STMFD sp!, {lr}	
	AND r3, r4, r11	;get 1 bit from multiplicand
	
	CMP r3, #1	;if r3 = 1
	ADDEQ r6, r6, r8	;add 
	
	LSL r8, #1	;shift left 
	
	
	ADD r1, r1, #1	;multiplicand count ++
	LSR r4, #1	;shift right multiplicand
	CMP r1, #8	;if count = 8

	BNE fraction_mul+4	;loop again, no STMFD
	LSREQ r6, #7	;erase 7bits
	MOV r12, #0		;shift stack
	BL fraction_normalize
	LDMFD sp!, {pc}
	
fraction_normalize	;normalize fraction
	STMFD sp!, {lr}
	;is bigger than 1.1111...
	LDR r11, =0x00FFFFFF	
	CMP r6, r11
	LSRGT r6, #1
	ADDGT r12, #1		;plus shift stack
	BGT fraction_normalize+4
	;is smaller than 1.00000....
	LDR r11, =0x00800000
	CMPLT r6, r11
	LSLLT r6, #1
	ADDLT r12, #-1	; minus shift stack
	BLT fraction_normalize+4
	LDMFD sp!, {pc}
	
adding	;r7 = r7+r9 
	STMFD sp!, {r0-r4, r8, lr}
	LDR r11, =0x80000000 ;get sign bit
	AND r0, r7, r11
	AND r1, r9, r11
	
	LDR r11, =0x7F800000	;get exponent bit
	AND r2, r7, r11
	AND r3, r9, r11
	
	LDR r11, =0x007FFFFF	;get fraction bit
	AND r4, r7, r11
	AND r5, r9, r11
	
	LDR r11, =0x00800000	;add 1.0 to fraction bit
	ADD r4, r4, r11
	ADD r5, r5, r11
	
	CMP r2, r3	;compare exponent
	LSR r2, #23	
	LSR r3, #23
	;result exponent = bigger one, shift(bigger - smaller) small one's fraction 
	;r6 = bigger-smaller
	SUBGE r6, r2, r3
	SUBLT r6, r3, r2
	;smaller shift amount r6
	LSRGE r5, r6
	LSRLT r4, r6	;
	ADD r10, r4, r5	;add
	
	MOV r12, #0 ; shift stack
	BL add_normalize
	
	CMP r2, r3	;find bigger exponent and add shift stack
	ADDGE r2, r2, r12
	LSLGE r2, #23
	ADDLT r3, r3, r12
	LSLLT r3, #23
	;add shift stack
	ADDGE r7, r0, r2
	ADDLT r7, r0, r3
	
	;erase 1.0 from fraction
	LDR r11, =0x007FFFFF
	AND r10, r10, r11
	ADD r7, r7, r10	;add exponent + sign bit and fraction bit
	
	LDMFD sp!, {r0-r4, r8, pc}
	
add_normalize	;normalize fraction
	STMFD sp!, {lr}
	LDR r11, =0x00FFFFFF	;is bigger than 1.11111....
	CMP r10, r11
	LSRGT r10, #1
	ADDGT r12, r12, #1	;shift stack ++
	BGT add_normalize+4	;no STMFD
	
	LDR r11, =0x00800000	;is smaller than 1.0000.....
	CMPLT r10, r11
	LSLLT r10, #1
	ADDLT r12, #-1	;shift stack --
	BLT add_normalize+4	;no STMFD
	LDMFD sp!, {pc}

sub_sampling	;sub sampling

	LDR r0, Matrix_result	;address of Matrix_result
	LDR r1, Matrix_convolution	;address of Matrix_convolution
	
	MOV r2, #0	;column count
	MOV r3, #0	;row count
	
	B sub_sampling_one	;loop to make Matrix_result
	
sub_sampling_one
	LDR r4, [r1], #8	;get convolution value, address++2, odd column
	STR r4, [r0], #4	;store in Matrix_result
	ADD r2, r2, #1		;column count ++
	CMP r2, #32			;does it 32 times loop? => 2 * 32 = 64
	BNE sub_sampling_one	;no, loop again
	ADDEQ r3, r3, #1	;row count ++
	ADDEQ r1, r1, #256	;address ++ 64. odd row
	MOVEQ r2, #0		;initialize column count
	CMPEQ r3, #32		;does it 32 times loop? => 2 * 32 = 64
	BNE sub_sampling_one	;no, loop again
	MOVEQ r5, #0		;	no operation
	MOV pc, #0	;program end
	
